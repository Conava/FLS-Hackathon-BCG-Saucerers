"""Integration test: Rebecca (PT0199) positive-trajectory showcase — T7.

Ingests the real ``data/`` directory against a live Postgres instance (via
testcontainers) and asserts that Rebecca's end-to-end story renders correctly:

  1. Exactly 90 wearable rows are loaded.
  2. The most recent wearable date is 2026-04-10 (last day of the 90-day arc).
  3. At least 60 daily_log rows exist (T5 generated 90).
  4. At least 10 meal_log rows exist (T5 generated 14).
  5. At least one meal_log row has a non-empty ``longevity_swap``.
  6. Every meal_log row has a ``photo_uri`` starting with ``manual://``.
  7. ``compute_vitality`` produces a composite score in the 70–85 band.
  8. The trend sparkline has at least 14 points (TREND_WINDOW_DAYS=30 → 30 pts).
  9. ``compute_outlook`` projections are monotonically non-decreasing across the
     3- / 6- / 12-month horizons, and each horizon is ≥ the current score.
 10. No projected score exceeds the CEILING of 95.0.

The test uses the real ``data/`` directory (``repo_root/data``); path resolution
is verified relative to this file's location:
``Path(__file__).resolve().parents[3] / "data"``
  → ``<repo>/backend/tests/integration/`` = __file__ parent
  → [0] = integration/
  → [1] = tests/
  → [2] = backend/
  → [3] = repo root
  → / "data" = repo_root/data

Run with::

    uv run pytest backend/tests/integration/test_rebecca_showcase.py -x
"""

