_DATA_DIR: Path = Path(__file__).resolve().parents[3] / "data"


# ---------------------------------------------------------------------------
# Helper: fetch LifestyleProfile for a patient via raw SQL
# ---------------------------------------------------------------------------


async def _get_lifestyle(session: AsyncSession, patient_id: str) -> LifestyleProfile | None:
    """Fetch the LifestyleProfile for ``patient_id``.

    There is no dedicated LifestyleProfileRepository — the model uses
    ``patient_id`` as its own PK, so we fetch it directly with a
    ``select`` statement following the project's ``getattr`` pattern for
    mypy-safe column comparisons.

    Args:
        session:    The active async session.
        patient_id: The patient to look up.

    Returns:
        The ``LifestyleProfile`` row, or ``None`` if not found.
    """
    pid_attr = getattr(LifestyleProfile, "patient_id")
    stmt = select(LifestyleProfile).where(pid_attr == patient_id)
    result = await session.execute(stmt)
    return result.scalars().first()


